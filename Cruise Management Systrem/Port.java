    // Port class represents a port destination
    public class Port {

        private String name;

        // Constructor
        public Port(String name) {
            this.name = name;
        }

        // Return of port name
        public String getName() {
            return name;
        }

        // Setter for port name
        public void setName(String name) {
            this.name = name;
        }
    }
