// Ship class represents a cruise ship
public class Ship {
    
    private String shipName;
    
    // Constructor
    public Ship(String shipName) {
        this.shipName = shipName;
    }
    
    // Return of ship name
    public String getShipName() {
        return shipName;
    }
    
    // Setter for ship name
    public void setShipName(String shipName) {
        this.shipName = shipName;
    }
}       
